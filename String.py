name = "Jakaria"
print(len(name))  # len for length of string
print(name.find("k"))  # find for searching from string
print(name.capitalize())  # capitalize for first letter capitale
print(name.upper())  # upper() for upper case of string
print(name.lower())  # lower() For lower case of all string
print(name.isalpha())  # isalpha() for is the variable is string or not
print(name.isdigit())  # isdigit() for is the variable is integer or not
print(name.count("a"))  # count("the name of searching") for
print(name.replace("J", "k"))  # replace("which element replace")
